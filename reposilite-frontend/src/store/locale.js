export const i18n = createI18n({
  legacy: false,
  locale: locale.current,
  fallbackLocale: 'en',
  messages
})

export default function useLocale() {
  const fetchLocale = () => {
    const storedLocale = localStorage.getItem(localeKey) ?? 'en'
    localStorage.setItem(localeKey, storedLocale)
    locale.current = storedLocale
    i18n.global.locale.value = storedLocale
  }

  const changeLocale = (code) => {
    localStorage.setItem(localeKey, code)
    locale.current = code
    i18n.global.locale.value = code
  }

  const t = (key) => {
    return i18n.global.t(key)
  }

  return {
    locale,
    fetchLocale,
    changeLocale,
    t
  }
}
